package controlStatements;

public class Switch {

	public static void main(String[] args) {
		int number=20;
		switch(number)
		{
		case 1:System.out.println("number is 1");
		break;
		case 10:System.out.println("number is 10");
		break;
		case 20:System.out.println("number is 20");
		break;
		case 30:System.out.println("number is 30");
		break;
		default: System.out.println("num is not in 1,10,20,30");
		}
	}

}
