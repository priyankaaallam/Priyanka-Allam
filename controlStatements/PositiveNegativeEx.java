package controlStatements;

public class PositiveNegativeEx {

	public static void main(String[] args) {
		int num=-23;
		if(num>0)
		{
			System.out.println("num is positive");
		}
		else if(num<0) {
			System.out.println("num is negative");
			
		}
		else {
			System.out.println("num is zero");
		}
		
	}

}
